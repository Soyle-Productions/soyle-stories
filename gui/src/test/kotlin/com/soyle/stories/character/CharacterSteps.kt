import io.cucumber.java8.PendingException

class CharacterSteps : En {

    init {

        Given("The character list tool has been opened") { throw PendingException() }

        Given("User has entered a name into the character name field") { throw PendingException() }

        When("A new character is built") { throw PendingException() }

        Then("The built character should be listed in the character list tool") { throw PendingException() }

    }

}